import mongoose from "mongoose";

const assetsSchema = new mongoose.Schema({
  _id: {
    type: String,
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  link: {
    type: String,
    required: true,
  },
  done_emails: [String],
});

const resourceSchema = new mongoose.Schema({
  _id: {
    type: String,
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  assets: [assetsSchema],
});

var subjectSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  resources: [resourceSchema],
});

mongoose.models = {};

var Subject =
  mongoose.models.subjects || mongoose.model("subjects", subjectSchema);
export default Subject;
